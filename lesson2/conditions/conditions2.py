# Приклади використання операторів порівняння

# Оператори порівняння використовуються для порівняння значень і повертають логічні значення (True або False).

# Приклад 1: Порівняння чисел
a = 5
b = 3

# Рівність (==)
result1 = a == b  # Перевірка, чи a рівне b
print(f"{a} == {b}: {result1}")

# Нерівність (!=)
result2 = a != b  # Перевірка, чи a не рівне b
print(f"{a} != {b}: {result2}")

# Більше за (>)
result3 = a > b  # Перевірка, чи a більше b
print(f"{a} > {b}: {result3}")

# Менше за (<)
result4 = a < b  # Перевірка, чи a менше b
print(f"{a} < {b}: {result4}")

# Більше або рівне (>=)
result5 = a >= b  # Перевірка, чи a більше або рівне b
print(f"{a} >= {b}: {result5}")

# Менше або рівне (<=)
result6 = a <= b  # Перевірка, чи a менше або рівне b
print(f"{a} <= {b}: {result6}")

# Приклад 2: Порівняння рядків
str1 = "apple"
str2 = "banana"

# Рівність (==)
result7 = str1 == str2  # Перевірка, чи str1 рівний str2
print(f'"{str1}" == "{str2}": {result7}')

# Нерівність (!=)
result8 = str1 != str2  # Перевірка, чи str1 не рівний str2
print(f'"{str1}" != "{str2}": {result8}')

# Лексикографічне порівняння
result9 = str1 < str2  # Порівняння рядків за лексикографічним порядком
print(f'"{str1}" < "{str2}": {result9}')
