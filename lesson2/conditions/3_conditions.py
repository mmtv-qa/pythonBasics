# Тернарний оператор - це спеціальний вид умовного виразу, який дозволяє вам зручно вибирати значення на основі умови.
# Вираз тернарного оператора має наступну структуру:
# [вираз_якщо_умова_істинна if умова else вираз_якщо_умова_хибна]


# Приклад 1
a = 5
b = 7
result = "a більше b" if a > b else "b більше а"
print(result)


# Приклад 2
number = 10
parity = "парне" if number % 2 == 0 else "непарне"
print(f"{number} - це {parity} число.")
